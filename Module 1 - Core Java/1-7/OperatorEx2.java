package com.a17;

public class OperatorEx2 
{
	public static void main(String[] args) 
	{
		
		//Assignment Op or Shorthand Op
		
		int a = 6;
		int b = 3;
		
		b=a;
		
		System.out.println(b);
		
		//b+=a -> b=b+a
		//c-=a -> c=c-a
		
		//ShortHand OpEx
		/*
		 	
		 	+=
		 	-=
		 	*=
		 	/=
		 			
		 			
		 */
		
		
		int c = 6;
		int d = 2;
		
		c+=d;//c=c+d
		System.out.println(c);//8
		
		c-=d;//c=c-d
		System.out.println(c);
		
		//c*=d;
		//c/=d;
		
		
	}
}
