package com.a17;

public class OperatorEx3 
{
	public static void main(String[] args) 
	{
		//Logical Operator Ex
		
		// &&, || , !
		
		int a = 6;
		int b = 5;
		int c = 3;
		
		System.out.println(a>b && b>c);
		System.out.println(a>b && b<c);
		System.out.println(a>b || b>c);
		System.out.println(a>b || b<c);
		System.out.println(a!=b);
		
		
	}
}
