package com.a17;

public class OperatorsEx 
{
	public static void main(String[] args) 
	{
		
		int a = 6;
		int b = 5;
		int c = 6;
		
		//Arithmetic Op -> +,-,*,/,%
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		//Relational Op -> <,>,<=,>=,==,!=
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=c);
		System.out.println(a>=c);
		System.out.println(a==b);
		System.out.println(a==c);
		System.out.println(a!=b);
		
		
	}
}
